package aula1;

public class Calculadora {
    private int mais;
    private int menos;


    public Calculadora() {
    }

    public int getMais() {
        return mais;
    }

    public void setMais(int mais) {
        this.mais = mais;
    }

    public int getMenos() {
        return menos;
    }

    public void setMenos(int menos) {
        this.menos = menos;
    }


}
